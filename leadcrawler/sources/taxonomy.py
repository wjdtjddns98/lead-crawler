"""닫힌 업종 대분류 택소노미 — 구분(엑셀 I열)의 단일 출처.

'전체(broad)' 검색 시 구분에 리터럴 '전체'가 찍혀 필터가 무가치해지던 문제를 고치기 위해,
발견된 모든 기업을 **닫힌 대분류 집합** 중 하나로 귀속시킨다. 닫힌 집합이라 표기가 분산되지
않아 필터가 제 기능을 한다(자유생성 라벨의 파편화 회피).

라벨을 채우는 경로(우선순위):
1. 등록처 코드 역매핑(:mod:`industry`) — 명확한 단일 매치만 확정(무료·정확).
2. AI 배치(:mod:`enrich.industry_classify`) — 코드가 없거나 모호/미분류/catch-all 이면 LLM 이
   회사 신호(이름·도메인·홈페이지)를 보고 이 목록 중 하나로 배치한다.
3. 그래도 불가하면 :data:`UNCLASSIFIED`.

:data:`AMBIGUOUS_LABELS` 는 "무조건 LLM 한번 거침" 대상 — 미분류 + catch-all('기타 제조'처럼
코드 매칭이 뭉뚱그린 라벨)을 담는다. 확신 단일 코드매치 라벨은 여기 없으므로 LLM 을 건너뛴다.
"""

