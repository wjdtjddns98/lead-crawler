# 빠른 멤버십 판정용(구성 시 1회). 라벨 문자열 일치 검사에 O(1).
_TAXONOMY_SET: frozenset[str] = frozenset(INDUSTRY_TAXONOMY)

# "무조건 LLM 한번 거침" 대상 — 미분류 + catch-all(코드 매칭이 뭉뚱그린 저정밀 라벨).
# 파이프라인은 회사 구분이 이 집합에 들면 LLM 재배치를 시도한다(확신 라벨은 스킵).
AMBIGUOUS_LABELS: frozenset[str] = frozenset({UNCLASSIFIED, "기타 제조"})


def is_taxonomy_label(value: str | None) -> bool:
    """값이 닫힌 대분류 집합(또는 '미분류')에 속하는 유효 구분값인지 판정한다."""
    if not value:
        return False
    v = value.strip()
    return v == UNCLASSIFIED or v in _TAXONOMY_SET
