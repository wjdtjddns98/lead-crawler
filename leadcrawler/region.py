"""주소 → 지역(1차 행정구역) 정규화.

발견 소스가 준 주소 원문에서 필터링 가능한 지역 라벨을 뽑는다.
- KR: 17개 시/도 별칭을 표준 축약형('서울', '부산', '경기' …)으로 정규화.
- 그 외 국가: 구조화 주소를 주는 소스(GLEIF city 등)가 region 을 직접 전달하므로
  주소 문자열 파싱은 하지 않는다(오탐 방지 — 국가별 주소 체계가 제각각).
"""

from __future__ import annotations

# 별칭 → 표준 축약형. startswith 매칭이므로 긴 별칭을 먼저 확인한다.
_KR_REGION_ALIASES: list[tuple[str, str]] = [
    ("서울특별시", "서울"),
    ("부산광역시", "부산"),
    ("대구광역시", "대구"),
    ("인천광역시", "인천"),
    ("광주광역시", "광주"),
    ("대전광역시", "대전"),
    ("울산광역시", "울산"),
    ("세종특별자치시", "세종"),
    ("경기도", "경기"),
    ("강원특별자치도", "강원"),
    ("강원도", "강원"),
    ("충청북도", "충북"),
    ("충청남도", "충남"),
    ("전북특별자치도", "전북"),
    ("전라북도", "전북"),
    ("전라남도", "전남"),
    ("경상북도", "경북"),
    ("경상남도", "경남"),
    ("제주특별자치도", "제주"),
    ("제주도", "제주"),
    ("서울시", "서울"),
    ("부산시", "부산"),
    ("대구시", "대구"),
    ("인천시", "인천"),
    ("광주시", "광주"),
    ("대전시", "대전"),
    ("울산시", "울산"),
    ("세종시", "세종"),
    ("서울", "서울"),
    ("부산", "부산"),
    ("대구", "대구"),
    ("인천", "인천"),
    # '광주'는 경기 광주시와 모호하지만 주소 선두 표기는 광역시가 관례 — 광역시로 본다.
    ("광주", "광주"),
    ("대전", "대전"),
    ("울산", "울산"),
    ("세종", "세종"),
    ("경기", "경기"),
    ("강원", "강원"),
    ("충북", "충북"),
    ("충남", "충남"),
    ("전북", "전북"),
    ("전남", "전남"),
    ("경북", "경북"),
    ("경남", "경남"),
    ("제주", "제주"),
]


def region_from_address(country: str, address: str | None) -> str | None:
    """주소 원문에서 지역 라벨을 뽑는다(현재 KR 만, 실패 시 None).

    KR 주소는 관례상 시/도로 시작한다("부산광역시 해운대구 …"). 선두 토큰을
    별칭 표로 정규화하며, 매칭 실패는 None (지역 미상 — 필터에서 제외될 뿐).
    """
    if not address:
        return None
    if country.strip().upper() != "KR":
        return None
    head = address.strip()
    for alias, canon in _KR_REGION_ALIASES:
        if head.startswith(alias):
            return canon
    return None
